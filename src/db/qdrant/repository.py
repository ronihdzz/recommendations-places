from typing import List, Dict, Any, Optional
from uuid import UUID
import time
from qdrant_client.models import Distance, VectorParams, PointStruct, Filter, FieldCondition, MatchValue
from qdrant_client.http.exceptions import UnexpectedResponse
from loguru import logger
from core.settings import settings
from .connection import get_qdrant_client


class PlaceEmbeddingRepository:
    """Repositorio para manejar embeddings de lugares en Qdrant"""
    
    def __init__(self, collection_name: str = None):
        """
        Inicializa el repositorio
        
        Args:
            collection_name: Nombre de la colecci√≥n en Qdrant (usa settings por defecto)
        """
        self.collection_name = collection_name or settings.QDRANT_COLLECTION_NAME
        self.client = get_qdrant_client()
        self.vector_size = 1536  # OpenAI text-embedding-3-small dimensions
        
        logger.debug(f"üóÑÔ∏è Repositorio Qdrant inicializado:")
        logger.debug(f"   üìä Colecci√≥n: {self.collection_name}")
        logger.debug(f"   üìè Tama√±o vector: {self.vector_size}")
        
    def create_collection(self):
        """Crea la colecci√≥n de embeddings si no existe"""
        try:
            logger.info(f"üîß Verificando colecci√≥n '{self.collection_name}'...")
            
            # Verificar si la colecci√≥n ya existe
            collections = self.client.get_collections()
            existing_collections = [col.name for col in collections.collections]
            
            logger.debug(f"üìä Colecciones existentes: {existing_collections}")
            
            if self.collection_name not in existing_collections:
                logger.info(f"üìù Creando nueva colecci√≥n '{self.collection_name}'...")
                self.client.create_collection(
                    collection_name=self.collection_name,
                    vectors_config=VectorParams(
                        size=self.vector_size,
                        distance=Distance.COSINE
                    )
                )
                logger.success(f"‚úÖ Colecci√≥n '{self.collection_name}' creada exitosamente")
            else:
                logger.info(f"‚úÖ Colecci√≥n '{self.collection_name}' ya existe")
                
        except Exception as e:
            logger.error(f"‚ùå Error al crear colecci√≥n '{self.collection_name}':")
            logger.error(f"   üî• Tipo: {type(e).__name__}")
            logger.error(f"   üìù Error: {e}")
            raise
    
    def upsert_embedding(self, place_id: str, embedding: List[float], metadata: Dict[str, Any]):
        """
        Inserta o actualiza un embedding de lugar
        
        Args:
            place_id: ID √∫nico del lugar
            embedding: Vector de embedding
            metadata: Metadatos del lugar (nombre, categor√≠a, etc.)
        """
        try:
            logger.debug(f"üíæ Guardando embedding para lugar ID: {place_id}")
            
            point = PointStruct(
                id=place_id,
                vector=embedding,
                payload=metadata
            )
            
            start_time = time.time()
            self.client.upsert(
                collection_name=self.collection_name,
                points=[point]
            )
            upsert_time = time.time() - start_time
            
            logger.debug(f"‚úÖ Embedding guardado ({upsert_time:.3f}s) - ID: {place_id}")
            
        except Exception as e:
            logger.error(f"‚ùå Error al hacer upsert del embedding:")
            logger.error(f"   üÜî Place ID: {place_id}")
            logger.error(f"   üìä Colecci√≥n: {self.collection_name}")
            logger.error(f"   üî• Tipo: {type(e).__name__}")
            logger.error(f"   üìù Error: {e}")
            raise
    
    def search_similar_places(
        self, 
        query_embedding: List[float], 
        limit: int = 5, 
        score_threshold: float = 0.0,
        filters: Dict[str, Any] = None
    ) -> List[Dict[str, Any]]:
        """
        Busca lugares similares basado en un embedding de consulta
        
        Args:
            query_embedding: Vector de embedding para buscar similares
            limit: N√∫mero m√°ximo de resultados
            score_threshold: Umbral m√≠nimo de similitud (0.0 - 1.0)
            filters: Filtros adicionales para la b√∫squeda
            
        Returns:
            Lista de diccionarios con id, score y payload de lugares similares
        """
        logger.info(f"üîç Iniciando b√∫squeda de lugares similares:")
        logger.info(f"   üìä Colecci√≥n: {self.collection_name}")
        logger.info(f"   üìè Dimensiones embedding: {len(query_embedding)}")
        logger.info(f"   üéØ L√≠mite: {limit}")
        logger.info(f"   üìä Umbral score: {score_threshold}")
        logger.info(f"   üîß Filtros: {filters if filters else 'Ninguno'}")
        
        try:
            # Verificar dimensiones del embedding
            if len(query_embedding) != self.vector_size:
                raise ValueError(f"Embedding debe tener {self.vector_size} dimensiones, recibido: {len(query_embedding)}")
            
            # Construir filtros si se proporcionan
            query_filter = None
            if filters:
                logger.debug(f"üîß Construyendo filtros: {filters}")
                conditions = []
                for key, value in filters.items():
                    conditions.append(
                        FieldCondition(
                            key=key,
                            match=MatchValue(value=value)
                        )
                    )
                if conditions:
                    query_filter = Filter(must=conditions)
            
            # Realizar b√∫squeda
            logger.debug(f"üöÄ Ejecutando b√∫squeda vectorial...")
            start_time = time.time()
            
            search_result = self.client.search(
                collection_name=self.collection_name,
                query_vector=query_embedding,
                limit=limit,
                score_threshold=score_threshold,
                query_filter=query_filter,
                with_payload=True  # Incluir metadatos en los resultados
            )
            
            search_time = time.time() - start_time
            logger.info(f"‚ö° B√∫squeda completada en {search_time:.3f}s")
            
            # Formatear resultados
            results = []
            for i, scored_point in enumerate(search_result):
                result = {
                    "id": scored_point.id,
                    "score": scored_point.score,
                    "payload": scored_point.payload
                }
                results.append(result)
                
                logger.debug(f"   üéØ #{i+1}: ID={scored_point.id}, Score={scored_point.score:.4f}")
            
            logger.success(f"‚úÖ Encontrados {len(results)} lugares similares (threshold: {score_threshold})")
            
            if len(results) == 0:
                logger.warning(f"‚ö†Ô∏è No se encontraron resultados:")
                logger.warning(f"   üéØ Considera reducir el score_threshold (actual: {score_threshold})")
                logger.warning(f"   üìä Verifica que la colecci√≥n tenga datos")
                
                # Informaci√≥n adicional de debug
                try:
                    collection_info = self.client.get_collection(self.collection_name)
                    logger.warning(f"   üìà Puntos en colecci√≥n: {collection_info.points_count}")
                except Exception as debug_e:
                    logger.warning(f"   ‚ùì No se pudo obtener info de colecci√≥n: {debug_e}")
            
            return results
            
        except ConnectionError as e:
            logger.error(f"üö´ Error de conexi√≥n durante b√∫squeda vectorial:")
            logger.error(f"   üìä Colecci√≥n: {self.collection_name}")
            logger.error(f"   üåê URL Qdrant: {getattr(self.client, '_client', {}).get('url', 'N/A')}")
            logger.error(f"   üî• Error: {e}")
            logger.error(f"   üí° Verifica que Qdrant est√© ejecut√°ndose y accesible")
            logger.error(f"   üîß Comando: docker-compose up qdrant")
            raise
            
        except UnexpectedResponse as e:
            logger.error(f"üö´ Respuesta inesperada de Qdrant durante b√∫squeda:")
            logger.error(f"   üìä Colecci√≥n: {self.collection_name}")
            logger.error(f"   üìÑ Status: {getattr(e, 'status_code', 'N/A')}")
            logger.error(f"   üìù Contenido: {getattr(e, 'content', 'N/A')}")
            logger.error(f"   üí° Verifica la configuraci√≥n y estado de Qdrant")
            raise
            
        except ValueError as e:
            logger.error(f"üö´ Error de validaci√≥n en b√∫squeda:")
            logger.error(f"   üìè Dimensiones esperadas: {self.vector_size}")
            logger.error(f"   üìè Dimensiones recibidas: {len(query_embedding) if query_embedding else 'N/A'}")
            logger.error(f"   üìù Error: {e}")
            raise
            
        except Exception as e:
            logger.error(f"üö´ Error inesperado en b√∫squeda de lugares similares:")
            logger.error(f"   üìä Colecci√≥n: {self.collection_name}")
            logger.error(f"   üî• Tipo: {type(e).__name__}")
            logger.error(f"   üìù Error: {e}")
            logger.error(f"   üîç Stack trace completo disponible en logs debug")
            raise
    
    def get_collection_info(self) -> Dict[str, Any]:
        """
        Obtiene informaci√≥n sobre la colecci√≥n
        
        Returns:
            Diccionario con informaci√≥n de la colecci√≥n
        """
        try:
            logger.debug(f"üìä Obteniendo informaci√≥n de colecci√≥n '{self.collection_name}'...")
            
            collection_info = self.client.get_collection(self.collection_name)
            
            info_dict = {
                "name": self.collection_name,
                "points_count": collection_info.points_count,
                "segments_count": collection_info.segments_count,
                "disk_data_size": collection_info.disk_data_size,
                "ram_data_size": collection_info.ram_data_size,
                "config": {
                    "vector_size": collection_info.config.params.vectors.size,
                    "distance": collection_info.config.params.vectors.distance.value
                }
            }
            
            logger.debug(f"‚úÖ Info obtenida - Puntos: {info_dict['points_count']}")
            return info_dict
            
        except Exception as e:
            logger.error(f"‚ùå Error obteniendo informaci√≥n de colecci√≥n:")
            logger.error(f"   üìä Colecci√≥n: {self.collection_name}")
            logger.error(f"   üî• Error: {e}")
            raise
    
    def delete_embedding(self, place_id: str) -> bool:
        """
        Elimina un embedding de lugar
        
        Args:
            place_id: ID del lugar a eliminar
            
        Returns:
            True si fue exitoso, False si no
        """
        try:
            self.client.delete(
                collection_name=self.collection_name,
                points_selector=[place_id]
            )
            logger.info(f"Embedding eliminado para lugar ID: {place_id}")
            return True
            
        except Exception as e:
            logger.error(f"Error eliminando embedding: {e}")
            return False 